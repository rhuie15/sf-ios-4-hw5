import UIKit

class ItemsTableViewController: UITableViewController, ItemSaver {

    var items:[String] = []
    
    override func viewDidLoad() {
        super.viewDidLoad()

    }
    
    
    func saveItem(item:String){
        items.append(item)
        tableView.reloadData()
    }
    

    // MARK: - Table view data source
/*
    override func numberOfSectionsInTableView(tableView: UITableView) -> Int {
        // #warning Incomplete implementation, return the number of sections
        return 0
    }
*/
    
    override func tableView(tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        // #warning Incomplete implementation, return the number of rows
        return items.count
    }
    
    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        
        let cell = tableView.dequeueReusableCellWithIdentifier("itemCellIdentifier", forIndexPath: indexPath)
    
        cell.textLabel?.text = items[indexPath.row]
        
        return cell
    }

    
    // MARK: - Navigation

    // In a storyboard-based application, you will often want to do a little preparation before navigation
    override func prepareForSegue(segue: UIStoryboardSegue, sender: AnyObject?) {
        // Get the new view controller using segue.destinationViewController.
        // Pass the selected object to the new view controller.
        let destinationViewController = segue.destinationViewController as! AddItemViewController
        destinationViewController.delegate = self
        
    }
    

}
